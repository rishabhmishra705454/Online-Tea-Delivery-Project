package com.rishabh.teadelivery.Helperclass;

public class StaticRV1Helperclass {

    private int image;
    private String text;

    public StaticRV1Helperclass(int image, String text) {
        this.image = image;
        this.text = text;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
